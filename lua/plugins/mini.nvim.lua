return {
  'nvim-mini/mini.nvim',
  version = false,
  config = function()
    require('mini.notify').setup()
    require('mini.tabline').setup()
    require('mini.cursorword').setup()
    require('mini.pairs').setup()
    require('mini.comment').setup()
    require('mini.surround').setup()
    require('mini.splitjoin').setup()
    require('mini.indentscope').setup({
      symbol = '│',
      options = { try_as_border = true },
    })
    local hipatterns = require('mini.hipatterns')
    hipatterns.setup({
      highlighters = {
        fixme = { pattern = '%f[%w]()FIXME()%f[%W]', group = 'MiniHipatternsFixme' },
        hack  = { pattern = '%f[%w]()HACK()%f[%W]',  group = 'MiniHipatternsHack'  },
        todo  = { pattern = '%f[%w]()TODO()%f[%W]',  group = 'MiniHipatternsTodo'  },
        note  = { pattern = '%f[%w]()NOTE()%f[%W]',  group = 'MiniHipatternsNote'  },

        hex_color = hipatterns.gen_highlighter.hex_color(),
      },
    })
    local starter = require('mini.starter')
      starter.setup({
        header = [[
         ..::::::::::::.              ...........              .....::::-+++++++++++++==-+++++++++==-:.
         ..:::...  . ..::..            ............................:-=+++++++++==-::-=++++++++++++=-
         ...    ....:::::::::..            ........................::--====---:....-=+++++++++++++++=
           ...:::::::::::::::::..             ........ ......  ..........  ..:=++++++++++++++++++=
         ..::::::::::::::::::::::::..              .................... ...:-=++++++++++++++++++++++
        ...:::::::::::::::::::::-----:::...                    ...::-==++++=++++++++++++++++++++
        ....::::::::::::::::::-----------====---::::::...::::::::::----------===============-===++++
        ....::::::::::::::::::----------====++++++==+==++++=====---:::-===++++++=======-::.    .:++++
        ....:::::::::::::-::::::--------====+++++++++++++++++==--::-==++++++======--:..    .:-=+++++
        .....::::::::::::---:::::-------=====+++++++++++*++++=---===========--::..      .:-=++++++++
        .......:::::......:::::::::------====++++++++*******++=++++===--::..        .::-=++++++++++++
        .............::::::..:::::::------===++++++++**********++=-..        ..::-===++++++++++++++++
         .........::::::::----:::::::-----===+++++++++++++++++++=:...::---===+++++++++++++++++++++++
         ...    ....      ......::::::::-----===++++++++++++++++=========+++++++++++++++++++++++++++++
          ..           .........:::::-----==+++++++++++++++++++++=====++++++++++++++++++++++++++++
                  ..    ...:==:::..::::-----==+++++++++++++++++++++=+==+++++++++++++++++***+++++++++
                ..::.  ...:-**+*+::--::-----==+++++++++++++++++++++++++++++++++++++********+++++++++
                .:::::. ...-+****=:---::-----=++++++++++++++++++++++++++++++++++*************++++++++
                .:-::::. ...:====---::::::--==+++++++++++++++++++++++++++++++********+*******++++++++
                . .:-::::..::::--::::--::::-=++++++++++++++++++++++++++++++***************++*+++++++++
               ....:::::::::-----::--::::::-++*********+++++++++++++++++*******************++++++++++
                ...::::::::--------:::::::::-=++++*******+++++++++++++*********************++++++++++
                ...::::::::----:--::::::::::-==+++++*****++++++++++++++*****************++++++++++++
                  ..:::::::::::::::::::::::::--==++++++++++++++++++++++++++************+++++++++++++=
                  ..::::::::::::::::::::::---==++++**+++++++++++++++++++++++++++**+++++++++++++++===
                    ...:::::::::::::::::::-----=++++++++++++++========++++++++++++++++++++++++++=====]],
        items = {},
        content_hooks = {
          starter.gen_hook.aligning('center', 'center'),
        },
        footer = function()
          local datetime = os.date('%d.%m.%y  %H:%M')
          local stats = (require('lazy').stats and require('lazy').stats()) or { startuptime = 0 }
          local ms = (math.floor(stats.startuptime * 100 + 0.5) / 100)
          local user = os.getenv("USER") or os.getenv("USERNAME") or "nerd"
          local h = tonumber(os.date('%H'))
          local status = (h < 5 and "night") or (h < 12 and "morning") or (h < 18 and "day") or "evening"
          return string.format('Neovim started %sms | %s | good %s, %s~', ms, datetime, status, user)
        end,
      })
  end
}
